#include <stdio.h> // Include the standard input-output header for printf and scanf functions

int main(void) {
    printf("Hello World!\n"); // Print "Hello World!" to the console
    printf("\\o/ \t\"Start is good somehow.\"\n"); // You see the happy dummy at start of this line? :D
    /*
    * You can use
    * \b	Backspace
    * \f	Form feed
    * \n	Newline
    * \r	Return
    * \t	Horizontal tab
    * \v	Vertical tab
    * \\	Backslash
    * \'	Single quotation mark
    * \"	Double quotation mark
    * \?	Question mark
    * \0	Null character
    */
    scanf("%c"); // Wait for user input before exiting
    return 0; // Return 0 to indicate successful execution
}
