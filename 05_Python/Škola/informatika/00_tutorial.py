# Integer variable
my_int = 10

# Float variable
my_float = 3.14

# Complex variable
my_complex = 2 + 3j


# String variable
my_string = "Hello, world!"


# Boolean variable
my_bool = True


# List variable
my_list = [1, 2, 3]


# Tuple variable
my_tuple = (4, 5, 6)


# Range variable
my_range = range(5)


# Set variable
my_set = {1, 2, 3}


# Frozen set variable
my_frozenset = frozenset([4, 5, 6])


# Dictionary variable
my_dict = {'key1': 'value1', 'key2': 'value2'}


# NoneType variable
my_none = None


# Bytes variable
my_bytes = b'Hello'


# Bytearray variable
my_bytearray = bytearray(5)


# Memoryview variable
my_memoryview = memoryview(bytes(5))

